// Functions in javascript

function add(a,b){
	return a+b;
}
function sub(a,b){
	return a-b;
}
function mult(a,b){
	return a*b;	
}
function div(a,b){
	return a/b;
}



console.log("Sum is --->",add(2,3));

console.log("Subtration is ---->",sub(7,3));

console.log("Multiplication is ---->",mult(3,3));

